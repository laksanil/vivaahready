import type { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: [
          '/api/',
          '/admin/',
          '/dashboard/',
          '/profile/',
          '/matches/',
          '/settings/',
          '/connections/',
          '/messages/',
          '/search/',
          '/reconsider/',
          '/verify',
          '/verify-email',
          '/feedback',
        ],
      },
      {
        userAgent: 'Googlebot',
        allow: '/',
      },
      {
        userAgent: 'Bingbot',
        allow: '/',
      },
    ],
    sitemap: 'https://vivaahready.com/sitemap.xml',
  }
}
